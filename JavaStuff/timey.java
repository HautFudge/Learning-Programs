public class timey {
  public static void main(String[] args) {

    int lastHour = 11;
    int lastMinute = 59;
    System.out.print("Number of minutes since midnight: ");
    System.out.println(lastHour * 60 + lastMinute);

    double y = 1 / 3;
    System.out.println(y);

    double z = 1.0 / 3;
    System.out.println(z);

    System.out.println("Addition with integers first:");
    System.out.println(1 + 2 + "Hello");

    System.out.println("Addition with string first:");
    System.out.println("Hello" + 1 + 2);


  }
}
